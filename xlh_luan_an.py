import colorsys

screen = turtle.Screen()
screen.title("海龟遥控器 - 用键盘控制海龟！")
screen.bgcolor("black")
screen.setup(800, 600)

t = turtle.Turtle()
t.shape("turtle")
t.color("cyan")
t.pensize(2)
t.speed(0)

step = 10
angle = 5
color_index = 0

def update_color():
    global color_index
    color = colorsys.hsv_to_rgb(color_index / 360, 1, 1)
    t.pencolor(color)
    color_index = (color_index + 3) % 360

def forward():
    update_color()
    t.forward(step)

def backward():
    update_color()
    t.backward(step)

def turn_left():
    t.left(angle)

def turn_right():
    t.right(angle)

def pen_toggle():
    if t.isdown():
        t.penup()
        t.color("gray")
    else:
        t.pendown()
        t.color("cyan")

def clear_drawing():
    t.clear()

def go_home():
    t.penup()
    t.home()
    t.pendown()
    t.color("cyan")

screen.onkey(forward, "Up")
screen.onkey(backward, "Down")
screen.onkey(turn_left, "Left")
screen.onkey(turn_right, "Right")
screen.onkey(forward, "w")
screen.onkey(backward, "s")
screen.onkey(turn_left, "a")
screen.onkey(turn_right, "d")
screen.onkey(pen_toggle, "space")
screen.onkey(clear_drawing, "c")
screen.onkey(go_home, "h")

screen.listen()

# 在屏幕左下角显示操作提示
hint = turtle.Turtle()
hint.hideturtle()
hint.penup()
hint.color("white")
hint.goto(-380, -270)
hint.write("↑↓←→/WASD：移动  空格：抬笔/落笔  C：清除  H：回原点",
           font=("Arial", 11, "normal"))

screen.mainloop()
